
#include <stdio.h>

/** \brief calcula la suma de dos numeros
 *
 * \param float  primer numero
 * \param float segundo numero
 * \return float resultado de la suma
 *
 */
float CalcularSuma(float, float);

/** \brief calcula la resta de dos numeros
 *
 * \param float primer numero
 * \param float segundo numero
 * \return float resultado de la resta
 *
 */
float CalcularResta(float, float);

/** \brief calcula la division de dos numeros
 *
 * \param float primer numero
 * \param float segundo numero
 * \return float resultado de la division
 *
 */
float CalcularDivision(float, float);

/** \brief calcula la multiplicacion de dos numeros
 *
 * \param float primer numero
 * \param float segundo numero
 * \return float ressultado de la multiplicacion
 *
 */
float CalcularMultiplicacion(float, float);

/** \brief calcula el factorial del primer numero
 *
 * \param float primer numero
 * \return double resultado del factorial
 *
 */
double CalcularFactorialUno(float);

/** \brief calcula el factorial del segundo numero
 *
 * \param float segundo numero
 * \return double resultado del factorial
 *
 */
double CalcularFactorialDos(float);

/** \brief verifica si el primer numero es entero o no
 *
 * \param numeroUno float primer numero
 * \return float resultado de si es entero o no
 *
 */
float VerificarTipoUno(float numeroUno);

/** \brief verifica si el segundo numero es entero o no
 *
 * \param numeroDos float segundo numero
 * \return float resultado de si es entero o no
 *
 */
float VerificarTipoDos(float numeroDos);





